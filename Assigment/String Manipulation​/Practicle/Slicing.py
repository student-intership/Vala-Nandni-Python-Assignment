string = "Hello, Python!"

print("Original String: ",string)

print("Slice [0:5]: ", string[0:5]) 
print("Slice [7:]: ", string[7:])  
print("Slice [:]: ", string[:]) 
print("Slice [-7:]: ",string[-7:]) 
print("Slice [::2]: ",string[::2])  
print("Reversed String [::-1]: ",string[::-1])  
